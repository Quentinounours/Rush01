#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	display_tab(int **tab)
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			ft_putchar(tab[i][j] + 48);
			if (j != 3)
				ft_putchar(32);
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
